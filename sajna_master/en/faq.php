				<strong>2. How accessible is the restaurant to the handicapped ?</strong><br/>
						No, we do not have a wheel chair access.<br/><br/>
						
				<strong>3. Are reservations required?</strong><br/>
						Reservations are not required but are highly recommended especially for parties and other celebrations. “Walk-ins” are always welcome and customers will be seated as soon as possible.<br/><br/>
						
				<strong>4. Do you have a tasting menu?</strong><br/>
						From time to time throughout the year, we feature a seasonal tasting menu. In addition, we will be happy to arrange something for you personally.
<br/><br/>
						
				<strong>5. I have food allergies. Can you accommodate me?</strong><br/>
						YES! It is essential for guests to inform their server of any food allergies as not every ingredient in a dish is listed on the menu. Even if you informed us when you made the reservation, please follow up with the server so that he can communicate your allergies to the chef and provide you with an outstanding meal that is safe for you to eat.
<br/><br/>
						
				<strong>6. I am on a special diet. Is there anything I can eat?</strong><br/>
						YES! Chef and other staff are happy to alter a menu preparation to accommodate dietary needs. Advance notice is helpful but not necessary. We encourage our guests to talk to their server about their particular dietary needs.<br/><br/>
						
