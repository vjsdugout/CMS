				<strong>1. Quel type de nourriture servez-vous ?</strong><br/>
						Nous servons de la nourriture indienne et bangladeshi contemporaine. La quantité d’huile, d’aromates et d’épices est répartie de manière harmonieuse et uniforme. La haute cuisine indienne n’a jamais d’excès de gras et d’épices, qui élimine la vraie délicatesse de la nourriture.
<br/><br/>
						
				<strong>2. Comment le restaurant est-il accessible aux handicapés?</strong><br/>
						Non, nous n'avons pas d’accès pour les fauteuils roulants.<br/><br/>
						
				<strong>3. Les réservations sont-elles exigées ?</strong><br/>
						Les réservations ne sont pas exigées mais sont extrêmement recommandées surtout pour les parties, soirées et les autres fêtes. Les clients n’ayant pas réservés sont toujours les bienvenus et seront installés le plus tôt possible.<br/><br/>
						
				<strong>4. Avez-vous un menu gastronomique ?</strong><br/>
						De temps en temps durant l’année, nous préparons un menu gastronomique de saison. Par ailleurs, nous serons heureux d'arranger quelque chose pour vous personnellement.
<br/><br/>
						
				<strong>5. J'ai des allergies alimentaires. Pouvez-vous me servir un repas qui me convienne?</strong><br/>
						OUI ! Il est essentiel pour les convives d’informer leur serveur de n'importe quelles allergies alimentaires du fait que chaque ingrédient n’est pas énuméré sur le menu. Même si vous nous avez informés quand vous avez fait la réservation, s'il vous plaît informez également le serveur pour qu'il puisse communiquer vos allergies au chef de cuisine et vous fournir un repas remarquable que vous puissiez manger sans problèmes.
<br/><br/>
						
				<strong>6. Je suis un régime spécial. Y a-t-il quelque chose que je puisse manger?</strong><br/>
						OUI ! Le chef de cuisine et le reste du personnel sont heureux de changer une préparation de menu pour accommoder des besoins diététiques. Il est utile de l’annoncer à l’avance mais pas obligatoire. Nous encourageons nos clients à parler à leur serveur de leurs besoins diététiques particuliers.<br/><br/>
						
				<strong>7. Avez-vous des offres de plats végétariens sur votre menu ?</strong><br/>
						OUI ! Nous avons plusieurs sélections qui sont faites sans viande, volaille ou poisson. Le Chef de cuisine veut et est capable d'accommoder des demandes spéciales de même que de changer des préparations pour répondre à une préférence de plat végétarien ou végétalien. Il est utile de nous en informer à l’avance mais pas obligatoire. Nous encourageons nos invités à parler à leur serveur de leurs besoins diététiques particuliers.<br/><br/>
						
				<strong>8. Etes-vous affilié à d’autres restaurants ?</strong><br/>
						Non, nous sommes un restaurant unique à Genève, Suisse mais nous avons une autre branche située au Bangladesh. ( www.sajnarestaurant.com )<br/><br/>
						
				<strong>9. Où avez-vous trouvé votre nom ?</strong><br/>
						Le nom vient d'un restaurant existant actuellement situé au Bangladesh. Pendant presque 15 ans, Sajna a été le plus fin et exclusif restaurant situé au Bangladesh. Au Sajna, vous pouvez apprécier nos saveurs différentes, délicieuses et exotiques de cuisines exclusivement indiennes.<br/><br/>
			
